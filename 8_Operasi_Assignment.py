#Operasi Assignment
a = 5 #ini adalah assignment
print("a = 5 Hasilnya : ",a)

a = 5 + 1
print("a = 5 + 1 Hasilnya : ",a)

a += 1 #artinya ditambah 1
print("a += Hasilnya : ",a)

a -= 2 #artinya dikurang 2
print("a -= Hasilnya : ",a)

a *= 2 #artinya dikali 2
print("a *= Hasilnya : ",a)

a /= 5 #artinya dibagi 5
print("a /= Hasilnya : ",a)

a **= 3 #artinya dipangkat 3
print("a **= Hasilnya : ",a)

b = 10
print("hasilnya : ", b)
b %= 3 #artinya sisa dari dibagi 3
print("b %= 3 Hasilnya : ", b)

b = 10
print("hasilnya : ", b)
b //= 3 #artinya bilangan bulat dari dibagi 3
print("b //= 3 Hasilnya : ", b)

